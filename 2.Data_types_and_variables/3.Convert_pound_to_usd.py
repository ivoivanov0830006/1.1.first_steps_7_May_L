from forex_python.converter import CurrencyRates

amount = int(input("Amount in GBP: "))
c = CurrencyRates()
current_rate_usd = c.get_rate("GBP", "USD")
result_usd = amount * current_rate_usd

print(f"{amount} GBP is {result_usd:.3f} USD")


